print(mutable_list)

#   - Измените элементы списка mutable_list.
mutable_list[0] = 2
mutable_list[1] = 3.5
mutable_list[2] = 'четыре'
mutable_list[3] = False * 5
mutable_list[4] = [6, 7]
mutable_list[5] = '890'
mutable_list[6] = (True + False) * True

#   - Выведите на экран измененный список mutable_list.
