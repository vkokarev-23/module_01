#   - Создайте переменную immutable_var и присвойте ей кортеж из нескольких элементов разных типов данных.
immutable_var = ( 1, 2.5, 'три', True * 4, [5, 6], '789', True )

#   - Выполните операции вывода кортежа immutable_var на экран.
print(immutable_var)

# 3. Изменение значений переменных:
#   - Попытайтесь изменить элементы кортежа immutable_var. Объясните, почему нельзя изменить значения элементов кортежа.

# Кортеж (tuple)- список неизменяемых элементов - указателей на некие объекты.
# Сами ссылки (указатели) изменять нельзя, поэтому нельзя изменять и объекты,
# на которые они указывают. Но если ссылка указывает на изменяемый объект,
# например список, то его (список) изменить можно, но не ссылку на него.
immutable_var[4][0] = 1
print(immutable_var)

# 4. Создание изменяемых структур данных:

#   - Создайте переменную mutable_list и присвойте ей список из нескольких элементов.
